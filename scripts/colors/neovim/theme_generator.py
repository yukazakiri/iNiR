"""
Generate Neovim colorscheme from Material You colors (material_colors.scss).
Outputs ~/.config/nvim/colors/iNiR.vim

The colorscheme is loaded via:
  colorscheme iNiR          (init.vim)
  vim.cmd("colorscheme iNiR") (init.lua)

Running Neovim instances are reloaded via SIGUSR1, which triggers:
  autocmd Signal SIGUSR1 colorscheme iNiR

Part of iNiR wallpaper theming pipeline.
Called from applycolor.sh.
"""

