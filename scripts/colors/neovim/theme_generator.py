INIT_VIM_SNIPPET = """\

" iNiR wallpaper theming - Neovim colorscheme
colorscheme iNiR
autocmd Signal SIGUSR1 colorscheme iNiR
"""

INIT_LUA_SNIPPET = """\

-- iNiR wallpaper theming - Neovim colorscheme
vim.cmd("colorscheme iNiR")
vim.api.nvim_create_autocmd("Signal", {
    pattern = "SIGUSR1",
    command = "colorscheme iNiR",
})
"""

COLORSCHEME_PATTERN_VIM = r"colorscheme\s+iNiR"
COLORSCHEME_PATTERN_LUA = r'colorscheme\s*["\']?iNiR'


def ensure_nvim_integration(nvim_config_dir: str) -> str:
    """
    Inject colorscheme + autocmd into init.vim or init.lua if not already present.
    Returns a human-readable status message.
    """
    config_dir = Path(nvim_config_dir)

    for fname, snippet, pattern in [
        ("init.vim", INIT_VIM_SNIPPET, COLORSCHEME_PATTERN_VIM),
        ("init.lua", INIT_LUA_SNIPPET, COLORSCHEME_PATTERN_LUA),
    ]:
        cfg = config_dir / fname
        if cfg.exists():
            content = cfg.read_text()
            if re.search(pattern, content):
                return f"already integrated ({fname})"
            cfg.write_text(content.rstrip("\n") + "\n" + snippet)
            return f"integrated into {fname}"

    return "no init.vim or init.lua found (skipped auto-integration)"


def reload_neovim() -> None:
    """Send SIGUSR1 to all running nvim processes."""
    try:
        result = subprocess.run(
            ["pkill", "--signal", "SIGUSR1", "-x", "nvim"],
            capture_output=True,
            timeout=3,
        )
        if result.returncode == 0:
            print("[neovim] Sent SIGUSR1 to running nvim instances")
    except Exception:
        pass


