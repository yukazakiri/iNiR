# ---------------------------------------------------------------------------
# Color helpers
# ---------------------------------------------------------------------------


def parse_scss_colors(scss_path: str) -> dict[str, str]:
    """Parse material_colors.scss and extract hex color variables."""
    colors: dict[str, str] = {}
    try:
        with open(scss_path, "r") as f:
            for line in f:
                match = re.match(r"\$(\w+):\s*(#[A-Fa-f0-9]{6});", line.strip())
                if match:
                    name, value = match.groups()
                    colors[name] = value.lower()
    except FileNotFoundError:
        print(f"Error: {scss_path} not found", file=sys.stderr)
        sys.exit(1)
    return colors


def hex_to_rgb(hex_color: str) -> tuple[int, int, int]:
    h = hex_color.lstrip("#")
    return (int(h[0:2], 16), int(h[2:4], 16), int(h[4:6], 16))


def blend(c1: str, c2: str, factor: float = 0.5) -> str:
    """Blend two hex colors. factor=0 → c1, factor=1 → c2."""
    r1, g1, b1 = hex_to_rgb(c1)
    r2, g2, b2 = hex_to_rgb(c2)
    r = int(r1 + (r2 - r1) * factor)
    g = int(g1 + (g2 - g1) * factor)
    b = int(b1 + (b2 - b1) * factor)
    return f"#{r:02x}{g:02x}{b:02x}"


