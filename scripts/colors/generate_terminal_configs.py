def parse_scss_colors(scss_path):
    """Parse material_colors.scss compatibility values."""
    colors = {}
    try:
        with open(scss_path, "r") as f:
            for line in f:
                # Match lines like: $term0: #282828;
                match = re.match(r"\$(\w+):\s*(#[A-Fa-f0-9]{6});", line.strip())
                if match:
                    name, value = match.groups()
                    colors[name] = value
    except FileNotFoundError:
        print(f"Error: Could not find {scss_path}", file=sys.stderr)
        sys.exit(1)
    return colors


def load_json_colors(json_path):
    try:
        with open(json_path, "r") as f:
            data = json.load(f)
            return {k: v for k, v in data.items() if isinstance(v, str)}
    except FileNotFoundError:
        return {}


def load_generator_colors(scss_path, palette_json_path, terminal_json_path):
    colors = parse_scss_colors(scss_path) if scss_path else {}
    palette_colors = load_json_colors(palette_json_path)
    terminal_colors = load_json_colors(terminal_json_path)

    # Explicit contracts should win over SCSS compatibility values.
    colors.update(palette_colors)
    colors.update(terminal_colors)
    return colors


def ensure_line_in_file(filepath, line_to_add, check_pattern=None, at_top=False):
    """Add a line to a file if it doesn't exist. Creates file if needed."""
    filepath = Path(filepath)
    filepath.parent.mkdir(parents=True, exist_ok=True)

    # Check if file exists and if line already present
    if filepath.exists():
        content = filepath.read_text()
        if check_pattern:
            if re.search(check_pattern, content):
                return False  # Already present
        elif line_to_add in content:
            return False  # Already present
    else:
        content = ""

    # Add line
    if at_top:
        # Add at the beginning of file
        content = line_to_add + "\n" + content
    else:
        # Add at the end
        if content and not content.endswith("\n"):
            content += "\n"
        content += line_to_add + "\n"

    filepath.write_text(content)
    return True  # Added


